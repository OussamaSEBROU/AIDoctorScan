// ================================================================
//  6 · Render results + Medical Guidance
// ================================================================
function renderResults(preds) {
  const sorted = [...preds].sort((a, b) => b.probability - a.probability);
  const top = sorted[0];
  const pct = (top.probability * 100).toFixed(1);

  // Classify top result
  const cl = top.className.toLowerCase();
  let type;
  if (cl.includes('yes') && !cl.includes('no'))       type = 'tumor';
  else if (cl.includes('no tumor') || (cl.includes('no ') && !cl.includes('not '))) type = 'clear';
  else type = 'notmri';

  const bannerData = {
    tumor:  { c:'danger',  icon: ICONS.danger,  tKey:'dx_tumor_t',  dKey:'dx_tumor_d' },
    clear:  { c:'success', icon: ICONS.success, tKey:'dx_clear_t',  dKey:'dx_clear_d' },
    notmri: { c:'warn',    icon: ICONS.warn,    tKey:'dx_notmri_t', dKey:'dx_notmri_d' },
  };
  const b = bannerData[type];

  // Build bars ONLY for tumor/clear — NOT for notmri
  let barsHTML = '';
  if (type !== 'notmri') {
    sorted.forEach(p => {
      const v = (p.probability * 100).toFixed(1);
      const n = p.className.toLowerCase();
      let cc = 'warn', lbl = p.className;
      if (n.includes('yes') && !n.includes('no'))       { cc = 'danger';  lbl = t('bar_tumor'); }
      else if (n.includes('no tumor') || (n.includes('no ') && !n.includes('not '))) { cc = 'success'; lbl = t('bar_clear'); }
      else if (n.includes('not '))                       { cc = 'warn';    lbl = t('bar_notmri'); }

      barsHTML += `
        <div class="bar-item">
          <div class="bar-row"><span class="bar-label"><span class="bar-dot ${cc}"></span>${lbl}</span><span class="bar-val ${cc}">${v}%</span></div>
          <div class="track"><div class="fill ${cc}" style="width:0%" data-w="${v}"></div></div>
        </div>`;
    });
    barsHTML = `<div class="bars">${barsHTML}</div>`;
  }

  // Medical guidance
  const guideKeys = {
    tumor:  { title: 'guide_tumor_title', body: 'guide_tumor_body' },
    clear:  { title: 'guide_clear_title', body: 'guide_clear_body' },
    notmri: { title: 'guide_notmri_title', body: 'guide_notmri_body' },
  };
  const gk = guideKeys[type];
  const guidanceHTML = `
    <div class="med-guide">
      <div class="med-guide-head">${ICONS.stethoscope} ${t(gk.title)}</div>
      <div class="med-guide-body">${t(gk.body, { pct })}</div>
    </div>`;

  // Timestamp
  const ts = new Date().toLocaleString(currentLang === 'ar' ? 'ar-SA' : currentLang === 'fr' ? 'fr-FR' : 'en-US', { hour:'2-digit', minute:'2-digit', day:'numeric', month:'short', year:'numeric' });

  $resultsBox.innerHTML = `
    <div class="results">
      <div class="res-card">
        <div class="res-head">
          <h3>${ICONS.pulse} ${t('res_title')}</h3>
          <span class="res-time">${ts}</span>
        </div>
        <div class="res-body">
          <div class="dx-banner ${b.c}">
            <div class="dx-ico">${b.icon}</div>
            <div class="dx-txt"><h4>${t(b.tKey)}</h4><p>${t(b.dKey, { pct })}</p></div>
          </div>
          ${barsHTML}
          ${guidanceHTML}
          <div class="disclaimer">
            <p>${t('disclaimer')}</p>
          </div>
        </div>
      </div>
    </div>`;

  // Animate bars
  if (type !== 'notmri') {
    requestAnimationFrame(() => {
      setTimeout(() => {
        document.querySelectorAll('.fill[data-w]').forEach(el => { el.style.width = el.dataset.w + '%'; });
      }, 80);
    });
  }

  $resultsBox.scrollIntoView({ behavior: 'smooth', block: 'start' });
}

