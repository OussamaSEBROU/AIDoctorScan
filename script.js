function toggleDarkMode() {
  document.body.classList.toggle('dark');
  const isDark = document.body.classList.contains('dark');
  localStorage.setItem('dais-theme', isDark ? 'dark' : 'light');
  document.querySelector('meta[name="theme-color"]').content = isDark ? '#0F1420' : '#2B7AE9';
}

function initTheme() {
  const saved = localStorage.getItem('dais-theme');
  if (saved === 'dark' || (!saved && window.matchMedia('(prefers-color-scheme: dark)').matches)) {
    document.body.classList.add('dark');
    document.querySelector('meta[name="theme-color"]').content = '#0F1420';
  }
}

// ================================================================
//  1 · Load model in background (non-blocking)
// ================================================================
function waitForLibs() {
  return new Promise((resolve) => {
    const check = () => {
      if (typeof tmImage !== 'undefined' && typeof tf !== 'undefined') resolve();
      else setTimeout(check, 200);
    };
    check();
  });
}

async function loadModel() {
  try {
    await waitForLibs();
    model = await tmImage.load(MODEL_PATH + 'model.json', MODEL_PATH + 'metadata.json');
    modelReady = true;
    $status.classList.remove('loading');
    $status.classList.add('ready');
    $statusText.textContent = t('status_ready');
    console.log('Model loaded —', model.getTotalClasses(), 'classes');
  } catch (e) {
    console.error('Model load error:', e);
    $statusText.textContent = t('status_error');
    $status.style.background = '#FFF0F0';
    $status.style.color = '#E54D4D';
  }
}

